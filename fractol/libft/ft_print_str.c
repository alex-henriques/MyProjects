#include "libft.h"

int	ft_print_str(const char *str)
{
	int	i;

	i = 0;
	if (str == 0)
	{
		write (1, "(null)", 6);
		return (6);
	}
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
